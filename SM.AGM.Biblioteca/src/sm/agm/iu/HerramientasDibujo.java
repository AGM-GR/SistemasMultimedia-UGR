package sm.agm.iu;

/**
 *Herramientas utilizadas para Dibujar y Editar en un Lienzo2DImagen.
 * @author Alejandro Guerrero Martínez
 */
public enum HerramientasDibujo {
    
    /**
     *Herramienta para dibujar un Punto.
     */
    PUNTO,

    /**
     *Herramienta para dibujar una Línea.
     */
    LINEA,

    /**
     *Herramienta para dibujar un Rectángulo.
     */
    RECTANGULO,

    /**
     *Herramienta para dibujar una Elipse.
     */
    ELIPSE,

    /**
     *Herramienta para dibujar una Curva con un punto de control.
     */
    CURVA,

    /**
     *Herramienta para dibujar un Texto.
     */
    TEXTO,

    /**
     *Herramienta para Modificar una forma.
     */
    SELECCION;
}
